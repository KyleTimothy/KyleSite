
///////////////////////////////////////////////////////////////////////////////////////Navy Marine ribbon
let NavyMarine = document.getElementById("navyAndMarineRibbon");

NavyMarine.addEventListener("mouseover", showMePic =>{

let img = document.createElement("img");
img.src = "https://upload.wikimedia.org/wikipedia/commons/7/76/Navy_and_Marine_Corps_Medal_ribbon.svg"

let src = document.getElementById("navyAndMarineRibbon")

src.appendChild(img)

})

   NavyMarine.addEventListener("mouseover", showMe =>{
 document.getElementById("navyAndMarineRibbon").innerHTML="Navy Marine Ribbon"

}) 


NavyMarine.addEventListener("mouseout", showMePic =>{

let img = document.createElement("img");
img.src = "https://upload.wikimedia.org/wikipedia/commons/7/76/Navy_and_Marine_Corps_Medal_ribbon.svg"

let src = document.getElementById("navyAndMarineRibbon")

src.appendChild(img)

NavyMarine.addEventListener("mouseout", showMe =>{
 document.getElementById("navyAndMarineRibbon").innerHTML=""


 src.appendChild(img)

}) 

})

